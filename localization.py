def t(key, default=None):
    """翻译英文/拼音术语为中文，找不到则返回原值"""
    if key is None:
        return default or ''
    if isinstance(key, (int, float)):
        return str(key)
    s = str(key)
    return CN.get(s, default if default is not None else s)
